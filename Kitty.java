public class Kitty{
  private String name;
  private int age;
  public Kitty(String StartName, int StartAge) {
    name = StartName;
    age = StartAge;
  }

    //constructor that takes the name and age of the Kitty.
  public String getName() {
    return name;
  }
  public int getAge() {
    return age;
  }

}
